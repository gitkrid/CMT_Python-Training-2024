# Eingabe
x = 3
y = 2

# Berechnungen
summe = x + y
division = x / y
exponent = x ** y
rest = y % x
gleich = x == y

print("Summe x und y:", summe)
print("x geteilt durch y:", division)
print("x hoch y:", exponent)
print("Rest von y geteilt durch x:", rest)
print("x ist gleich wie y:", gleich)
